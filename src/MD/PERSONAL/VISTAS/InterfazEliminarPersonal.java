    private void btnEliminarPersonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPersonalActionPerformed
        try{
            if(cmbEliminarPersonal.getSelectedIndex()==0){
                showMessageDialog(this,"Por favor selecciona a un dato a eliminar");
            }else{
                int decision=showConfirmDialog(this,"Â¿Esta seguro que desea eliminar este dato?");
        if(decision==0){
            if(metodo.eliminarPersonal(cmbEliminarPersonal.getSelectedItem().toString())){
                javax.swing.JOptionPane.showMessageDialog(null, "Dato eliminado con exito");
                
               llenarPersonal();
            }
             }
            }
        }catch(Exception e){}
    }//GEN-LAST:event_btnEliminarPersonalActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            int filas=modeloTabla.getRowCount();
            for(int i=0;filas>i;i++){
                modeloTabla.removeRow(0);
            }
            if(modeloTabla.getRowCount()==0){
                metodo.llenadoConsultaPersonalCurp(modeloTabla);
                showMessageDialog(this,"Datos Actualizados");
            }else{
                showMessageDialog(this,"No fue posible actualizar la tabla");
            }
        } catch (SQLException ex) {
            Logger.getLogger(InterfazEliminarPersonal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

