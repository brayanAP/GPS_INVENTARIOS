    public void llenarPersonal(){
     try {
        ArrayList<String > personasDisponibles = metodo.extraerPersonal();
        cmbEliminarPersonal.removeAllItems();
        cmbEliminarPersonal.addItem("Seleccione a la persona");
        for(int i=0; i<personasDisponibles.size();i++){
        cmbEliminarPersonal.addItem(personasDisponibles.get(i));
          
                
        }
        
        } catch (SQLException ex) {
        Logger.getLogger(InterfazEliminarPersonal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
