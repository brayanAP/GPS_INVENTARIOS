public void llenarPersonalModif(){
     try {
        ArrayList<String > personasDisponibles = metodo.extraerPersonal();
        cmbModifPersonal.removeAllItems();
        cmbModifPersonal.addItem("Seleccione la Persona");
        for(int i=0; i<personasDisponibles.size();i++){
        cmbModifPersonal.addItem(personasDisponibles.get(i));
        }
        
        } catch (SQLException ex) {
        Logger.getLogger(InterfazModificarPersonal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
