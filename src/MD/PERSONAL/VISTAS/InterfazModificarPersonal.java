    private void btnRegistarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarPActionPerformed
if(cmbModifPersonal.getSelectedIndex()!=0){
       if(c2.isEnabled()==true&&p1.isVisible()==true||c3.isEnabled()==true&&p2.isVisible()==true||
               c4.isEnabled()==true&&p3.isVisible()==true || c5.isEnabled()==true&&p4.isVisible()==true
               ||c6.isEnabled()==true&&p5.isVisible()==true||c7.isEnabled()&&p6.isVisible()==true
               ||c8.isEnabled()&&p7.isVisible()==true||c9.isEnabled()==true&&p8.isVisible()==true
               ||c10.isEnabled()==true&&p9.isVisible()==true||c11.isEnabled()==true&&p10.isVisible()==true
               ||c12.isEnabled()==true&&p11.isVisible()==true||c13.isEnabled()&&p12.isVisible()==true
               ||c14.isEnabled()==true&&p13.isVisible()==true||c15.isEnabled()==true&&p14.isVisible()==true
               ||c1.isEnabled()==true&&p15.isVisible()==true){

            Personal persona = new Personal(txtNombreP.getText(),
                txtApellidoPatP.getText(),
                txtApellidoMat.getText(),
                txtCalleP.getText(),
                txtNumP.getText(),
                txtColP.getText(),
                Integer.parseInt(txtcpP.getText()),
                txtTelP.getText(),
                txtFechaNacP.getText(),
                txtCurpP.getText(),
                txtRfcP.getText(),
                txtLicMP.getText(),
                txtCredencialP.getText(),
                cmbAreaP.getSelectedIndex(),
                cmbPuestoP.getSelectedIndex());

           
     metodo.modificarPersonal(persona,cmbModifPersonal,cmbAreaP,cmbPuestoP);
     limpiarDatos();
       }else {
           if(cmbModifPersonal.getSelectedIndex()==0){
             showMessageDialog(this,"Por favor seleccione la persona a la cual desea modificar sus datos");
     
           }
           showMessageDialog(this,"Por favor seleccione un dato a modificar");
       }
     
}else{
      showMessageDialog(this,"Por favor seleccione la persona a la cual desea modificar sus datos");
      
}
    }//GEN-LAST:event_btnRegistarPActionPerformed

