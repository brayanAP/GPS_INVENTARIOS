    private void cmbTipoUsuItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTipoUsuItemStateChanged
      if(cmbTipoUsu.getSelectedIndex()!=0){
          p2.setVisible(true);
          
      }else if(cmbTipoUsu.getSelectedIndex()==0){
          p2.setVisible(false);
      }
      
      
      //validar
        if(lblContraseña.getText()!=null&&p1.isVisible()==false||p2.isVisible()==false||cmbPersonal.getSelectedIndex()==0){
            cbVerificar.setEnabled(false);
        }else{
        cbVerificar.setEnabled(true);
            
        }
    }//GEN-LAST:event_cmbTipoUsuItemStateChanged

    private void btnGenerarPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarPassActionPerformed
   if(p1.isVisible()==true&&p2.isVisible()==true){
   String contraseña="";
   contraseña=funcion.creaPass();
   
   lblContraseña.setText(contraseña);
   lblContraseña.setVisible(true);
   cbVerificar.setEnabled(true);
   }else{
       showMessageDialog(null,"Por favor verifica los campos");
       
   }
    }//GEN-LAST:event_btnGenerarPassActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
if(cbVerificar.isSelected()==true && lblContraseña.getText()!=null){
        int decision=showConfirmDialog(this,"¿Esta seguro que quiere desea registrar este usuario?");
         
        if(decision==0){
            Usuario usuario=new Usuario(txtNombreUsuario.getText(),lblContraseña.getText(),cmbPersonal.getSelectedIndex(),cmbTipoUsu.getSelectedIndex());
        if(metodo.insertUsuario(usuario)){showMessageDialog(this,"Usuario Registrado con Exito");}
         cmbPersonal.setSelectedIndex(0);
         txtNombreUsuario.setText("");
         txtNombreUsuario.setEnabled(false);
         p1.setVisible(false);
         cmbTipoUsu.setSelectedIndex(0);
         p2.setVisible(false);
        lblContraseña.setText("");
        lblContraseña.setVisible(false);
        cbVerificar.setEnabled(false);
        }
}else{
    showMessageDialog(this,"No se puede procesar por que:"+"\n"+"No ha seleccionado una persona,"+"\n"+"O un campo esta vacio,"+"\n"+"O El nombre de usuario no esta en mayusculas o no es mayor a 5 letras,"
            + ""+"\n"+"O no a seleccionado un rol,"+"\n"+"O no ha generado su contraseña,"+"\n"+"O no a verificado que ha guardado su contraseña y usuario");
}
    }//GEN-LAST:event_jButton2ActionPerformed

