    private void cmbPersonalItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbPersonalItemStateChanged
      if(cmbPersonal.getSelectedIndex()!=0){
          txtNombreUsuario.setEnabled(true);
      }
      
      
        if(lblContraseña.getText()!=null&&p1.isVisible()==false||p2.isVisible()==false||cmbPersonal.getSelectedIndex()==0){
            cbVerificar.setEnabled(false);
        }else{
        cbVerificar.setEnabled(true);
            
        }
    }//GEN-LAST:event_cmbPersonalItemStateChanged

