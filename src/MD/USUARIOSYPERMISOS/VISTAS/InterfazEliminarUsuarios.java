    public void llenarUsuarios(){
     try {
        ArrayList<String > usuariosDisponibles = metodoU.extraerUsuarios();
        cmbEliminarUsuario.removeAllItems();
        cmbEliminarUsuario.addItem("Seleccione el usuario");
        for(int i=0; i<usuariosDisponibles.size();i++){
        cmbEliminarUsuario.addItem(usuariosDisponibles.get(i));
          
                
        }
        
        } catch (SQLException ex) {
        Logger.getLogger(InterfazEliminarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
