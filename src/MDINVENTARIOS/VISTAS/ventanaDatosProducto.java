    public ventanaDatosProducto(String nombre,String marca,String modelo) {
        this.setUndecorated(false);
        initComponents();
        
        Producto pro = BDDInventarios.selectProductoFiltrado(nombre,marca,modelo);
        txtcol_pro.setText(pro.getCol_pro());
        txtdes_pro.setText(pro.getDes_pro());
        txtfechacom_pro.setText(pro.getFechcompra_pro());
        txtimp_pro.setText(pro.getImp_pro()+"");
        txtkilm_pro.setText(pro.getKm_pro()+"");
        txtmar_pro.setText(pro.getMar_pro());
        txtmod_pro.setText(pro.getMod_pro());
        txtnofac_pro.setText(pro.getNofact_pro());
        txtnoiden_pro.setText(pro.getCon_pro()+"");
        txtnom_pro.setText(pro.getNom_pro());
        txtnomor_pro.setText(pro.getNomot_pro());
        txtnoserie_pro.setText(pro.getSer_pro());
        txtobs_pro.setText(pro.getObs_pro());
        txtpla_pro.setText(pro.getPla_pro());
        txtservkm_pro.setText(pro.getKm_ser_pro()+"");
        txtstock_pro.setText(pro.getStock_pro()+"");
        txtstockmin_pro.setText(pro.getStockmin_pro()+"");
        txtsubcat_pro.setText(pro.getSubcat_pro());
        
        ArrayList<Clasificacion> clas = BDDInventarios.selectClasificaciones();
        ArrayList<Status> sta = BDDInventarios.selectStatus();
        
        for(int x=0; x < clas.size(); x++){
            Clasificacion temp = clas.get(x);
            this.cmbclasificacion.addItem(temp.getNom_clas());
            if(temp.getId_clas() == pro.getId_cla()){
                cmbclasificacion.setSelectedItem(temp.getNom_clas());
            }
        }
        
        for(int x=0; x < sta.size(); x++){
            Status temp = sta.get(x);
            this.cmbstatus.addItem(temp.getNom_sta());
            if(temp.getId_sta() == pro.getId_sta()){
                cmbstatus.setSelectedItem(temp.getNom_sta());
            }
        }
        
        ArrayList<Foto> foto = BDDInventarios.selectFotos();
        
        for(int x=0; x < foto.size(); x++){
            if (foto.get(x).getId_pro() == pro.getId_pro()){
                ImageIcon fot = new ImageIcon(foto.get(x).getFot_fot());
                Icon icono = new ImageIcon(fot.getImage().getScaledInstance(this.pnlimagenproducto.getWidth(), pnlimagenproducto.getHeight(), Image.SCALE_DEFAULT));
                JLabel etiqueta = new JLabel(icono);
                etiqueta.setSize(pnlimagenproducto.getWidth(), pnlimagenproducto.getHeight());
                pnlimagenproducto.removeAll();
                pnlimagenproducto.add(etiqueta).repaint();
                existefoto = true;
            }
        }
        
        desbloquearBloquearDatos(false);
        
        id = pro.getId_pro();
        
    }

