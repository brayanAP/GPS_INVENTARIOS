    private void btnCambiarFotografiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCambiarFotografiaMouseClicked
        String ruta = "";
        JFileChooser file=new JFileChooser();
        file.setDialogTitle("Cargar Imagen...");
        int seleccion = file.showSaveDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION){
            File imagen = file.getSelectedFile();
            ruta = imagen.getPath();
            ImageIcon fot = new ImageIcon(ruta);
            Icon icono = new ImageIcon(fot.getImage().getScaledInstance(pnlimagenproducto.getWidth(), pnlimagenproducto.getHeight(), Image.SCALE_DEFAULT));
            JLabel etiqueta = new JLabel(icono);
            etiqueta.setSize(pnlimagenproducto.getWidth(), pnlimagenproducto.getHeight());
            pnlimagenproducto.removeAll();
            pnlimagenproducto.add(etiqueta).repaint();
        }
        
        MDINVENTARIOS mi = new MDINVENTARIOS();
        String ft = mi.modificarFoto(ruta, id);
        javax.swing.JOptionPane.showMessageDialog(this, ft);
    }//GEN-LAST:event_btnCambiarFotografiaMouseClicked
    
