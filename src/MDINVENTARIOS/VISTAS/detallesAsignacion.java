    private void btnEliminarMovimientoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMovimientoMouseClicked

        int decision=showConfirmDialog(this,"¿Está seguro que desea eliminar este movimiento?");
        if(decision==0){
            boolean respuesta = BDDInventarios.deleteMovimiento(id);
            
            if(respuesta){
                javax.swing.JOptionPane.showMessageDialog(this, "Movimiento eliminado con éxito.");
                ventanaMovimientos.isAbiertodetallesAsignacion = false;
                dispose();
            }else{
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo eliminar el movimiento.\n" +"NOTA: Asegúrese de que todos sus productos asignados ya haiga sido quitado de la lista.");
            }
        }
    }//GEN-LAST:event_btnEliminarMovimientoMouseClicked

