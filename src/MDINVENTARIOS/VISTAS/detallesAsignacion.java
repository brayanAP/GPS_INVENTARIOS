    public detallesAsignacion(int id) {
        initComponents();
        
        this.id = id;
        
        Movimiento movimiento = BDDInventarios.selectMovimientoFiltrado(id);
        
        this.txtFecha.setText(movimiento.getFec_mov());
        this.txtPersona.setText(BDDInventarios.selectNombrePersonaMovimiento(movimiento.getId_per()));
        this.checkStatus.setSelected(movimiento.getSta_mov());
        this.txtObservaciones.setText(movimiento.getObs_mov());
        
        llenarLista(BDDInventarios.selectProductosAsignadosMovimiento(id));
        
        desbloquearDatosLista(false);
        desbloquearDatosFormulario(false);
        
        
    }
    
    public void llenarLista(ArrayList<String> arrayProductos){
        DefaultListModel modelo = new DefaultListModel();
       
        for(int i = 0; i < arrayProductos.size(); i++){
            modelo.addElement(arrayProductos.get(i));
        }
        
        this.listaProductos.setModel(modelo);
    }//llenarLista}
    
    
    public void desbloquearDatosLista(boolean valor){
        this.listaProductos.setEnabled(valor);
        this.btnEditarCantidad.setEnabled(valor);
        this.btnQuitarListta.setEnabled(valor);
    }//desbloquearDatosLista
    
    public void desbloquearDatosFormulario(boolean valor){
        this.txtFecha.setEnabled(false);
        this.txtPersona.setEnabled(false);
        this.checkStatus.setEnabled(valor);
        this.txtObservaciones.setEditable(valor);
        this.btnEditar.setEnabled(valor);
    }//desbloquearDatosLista

