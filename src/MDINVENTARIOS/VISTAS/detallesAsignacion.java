    private void btnQuitarListtaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnQuitarListtaMouseClicked
        try {
            DefaultListModel modeloLista = (DefaultListModel)listaProductos.getModel();
            int index =  this.listaProductos.getSelectedIndex();
            String datos = (String) modeloLista.getElementAt(index);

            String[] temp = datos.split("/");
            String nombre = temp[0];
            String marca = temp[1];
            String modelo = temp[2];
            int idProducto = BDDInventarios.buscarIdProducto(nombre, marca, modelo);
            int cantidad = Integer.parseInt(temp[3]);
            
            boolean respuesta = BDDInventarios.deleteProductoAsignado(id, idProducto);          
            
            if(respuesta){
                javax.swing.JOptionPane.showMessageDialog(this, "Producto quitado con éxito.");
                llenarLista(BDDInventarios.selectProductosAsignadosMovimiento(id));
            }else{
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo quitar el producto.");
            }
        }catch(Exception ee){
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un producto.");
        }
    }//GEN-LAST:event_btnQuitarListtaMouseClicked

    private void btnEditarCantidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarCantidadMouseClicked
        try {
            DefaultListModel modeloLista = (DefaultListModel)listaProductos.getModel();
            int index =  this.listaProductos.getSelectedIndex();
            String datos = (String) modeloLista.getElementAt(index);

            String[] temp = datos.split("/");
            String nombre = temp[0];
            String marca = temp[1];
            String modelo = temp[2];
            int id_pro = BDDInventarios.buscarIdProducto(nombre, marca, modelo);
            int cantidad;
                    try{
                        cantidad = Integer.parseInt(javax.swing.JOptionPane.showInputDialog("¿Qué cantidad?"));
                        if (cantidad < 0) cantidad = 1;
                    }catch(NumberFormatException ee){
                            cantidad = 1;
                    }
            
            boolean respuesta = BDDInventarios.updateCantidadProductoAsignado(id, id_pro, cantidad);    
            
            if(respuesta){
                javax.swing.JOptionPane.showMessageDialog(this, "Cantidad editada con éxito.");
                llenarLista(BDDInventarios.selectProductosAsignadosMovimiento(id));
            }else{
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo cambiar la cantidad, verifique que el producto tenga suficiente stock.");
            }
        }catch(Exception ee){
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un producto.");
        }
    }//GEN-LAST:event_btnEditarCantidadMouseClicked

